function User(
  id,
  taikhoan,
  matkhau,
  hoten,
  Email,
  image,
  ngonngu,
  loaiND,
  mota
) {
  this.id = id;
  this.taikhoan = taikhoan;
  this.matkhau = matkhau;
  this.hoten = hoten;
  this.Email = Email;
  this.image = image;
  this.ngonngu = ngonngu;
  this.loaiND = loaiND;
  this.mota = mota;
}
